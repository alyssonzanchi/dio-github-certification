# Desafio 2: Repetindo Textos ✏️

# Recebendo a string do usuário
texto = input("Digite a string que deseja repetir: ")

# Recebendo o número de repetições
numero_repeticoes = int(input("Digite o número de vezes que deseja repetir a string: "))

# Repetindo a string
resultado = texto * numero_repeticoes

# Exibindo o resultado
print("Resultado da repetição:", resultado)
